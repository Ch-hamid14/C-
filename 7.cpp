#include<iostream>
using namespace std;
main(){
	int num1,num2;
	cout<<"Enter a first number : ";
	cin>>num1;
	cout<<"Enter a second number : ";
	cin>>num2;
	cout<<endl;
	cout<<"Before Swaping"<<endl;
	cout<<"First number is : "<<num1<<endl;
	cout<<"Second number is : "<<num2<<endl;
	cout<<endl;
	cout<<"After Swapping"<<endl;
	cout<<num1+num2-num1<<endl;
	cout<<num1+num2-num2;
}
